package Kent_RO;

	// Program to convert milli second to minutes
import java.util.Scanner;
public class Q1 {
		
public static void main(String...s) {

		Scanner sc = new Scanner(System.in);	
	System.out.println("Enter Any value in milli Second");

		double a=sc.nextDouble();
		double b=(a/(1000*60));
		System.out.println("Value in minutes:" +b);
		
		
		
	}
	}
	